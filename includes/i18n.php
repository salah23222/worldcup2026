/**
 * t() — يرجّع نصاً مترجماً حسب اللغة الحالية.
 */
function t(string $key): string {
    static $dict = null;
    if ($dict === null) {
        $all  = translations();
        $dict = $all[current_lang()] ?? $all['ar'];
    }
    return $dict[$key] ?? $key;
}

/** اتجاه الصفحة rtl/ltr */
function lang_dir(): string {
    return t('dir');
}
